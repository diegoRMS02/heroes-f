class PowerStats {
  late int? intelligence;
  late int? strength;
  late int? speed;
  late int? durability;
  late int? power;
  late int? combat;

  PowerStats(intelligence, strength, speed, durability, power, combat) {
    this.intelligence = intelligence;
    this.strength = strength;
    this.speed = speed;
    this.durability = durability;
    this.power = power;
    this.combat = combat;
  }
}
